
/* Drop Tables */

DROP TABLE CAR;
DROP TABLE SHOPPINGLIST;




/* Create Tables */

CREATE TABLE CAR
(
	CARID INT NOT NULL AUTO_INCREMENT,
	PINPAIMING VARCHAR(20),
	XILIE VARCHAR(20),
	SHOUJIA INT,
	GONGLISHU INT,
	YANSE VARCHAR(20),
	PAILIANG VARCHAR(20),
	PRIMARY KEY (CARID)
);


CREATE TABLE SHOPPINGLIST
(
	CARID INT NOT NULL,
	PRINCE INT NOT NULL,
	YANSE VARCHAR(20) NOT NULL,
	PRIMARY KEY (CARID)
);



